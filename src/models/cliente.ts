class Cliente{
    nombre: string;
    direccion: string;
    email : string;
    tel : number;
    constructor( 
        nombre:string, 
        direccion:string,
        email : string,
        tel : number
    )
    {
 
        this.nombre = nombre;
        this.direccion = direccion;
        this.email = email
        this.tel = tel
    }

}
